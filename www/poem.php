          Но постоянно мы находим лишь баяны.
          А с баянАми в тендер не берут.
          
          Мы ищем Биг Айдию. Сука, где ты?!
          Ведь рядом, рядом Биг Айдии след!
          Мы гуглим и фейсбучим Интернеты,
          Но в Скайпе нет. И в Инстаграмме нет.
          
          Мы ищем Биг Айдию для клиента,
          Без Биг Айдии будет бренд не бренд.
          Мы чекаем инсайд, ресерчим тренды…
          Но снова не доступен абонент.
          
          Мы ищем Биг Айдию. Это важно,
          Чтоб взять гран-при и получить бабло!
          В гламуре ищем, в сексе, в эпатаже…
          Но снова фейк. Опять не повезло. 
          
          Мы ищем Биг Айдию нашей жизни.
          Ведь скоро кончится! Что там осталось жить?!
          Ну что же ты, паскуда, так капризна?!
          Приди ко мне! Отдайся мне! Найдись!
          
          Ну хватит прятаться, ну хватит маскарада!
          Ну обозначься, ну подай хоть знак!..
          А Биг Айдия плачет где-то рядом
          И думает: - Какой же ты мудак!
          
          Ведь вот же я! Твоя. Твоя с рождения.
          Возьми меня! Нам будет хорошо!
          Но что случилось у тебя со зреньем?
          Везде искал, а рядом не нашел.
          
          Мы страстно стать хотим звездой тусовки,
          Мы фотошопимся, чтоб вызвать восхищение.
          Но выясняется – все это лишь массовка,
          И схожи мы до степени смешения.
          
          Мы копирайты суетливо креативим,
          Мы эсэмэсим, лайкаем посты…
          А где-то рядом плачет Биг Айдия,
          Недостижимая из нашей суеты.
          
          Мы расширители сознанья принимаем,
          Ломаем рамки, мыслим вне коробки!..
          Но что-то главное никак не догоняем.
          Мы ограничены режимом блокировки.
          
          Но как же перезагрузить систему,
          Чтоб с чистого листа, начистоту?!
          Не креативить навороченные схемы,
          Взять курс на правду, тренд на простоту.
          
          И чтоб найти, увидеть Биг Айдию,
          С которой не страшна ни жизнь, ни смерть.
          И чтоб фонтаны били голубые…
          Ведь скоро кончится. Хотелось бы успеть.</p>
