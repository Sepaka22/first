          <h2>ФИНАЛЬНАЯ ПРЕЗЕНТАЦИЯ ДЛЯ АПОСТОЛА ПАВЛА</h2>
          <p>Когда очередь подошла, 
          Я вошел. Чемоданчик поставил.
          Некто в белом сидит у стола,
          И на пальцах наколка: «П-А-В-Е-Л».
          
          Он сказал: - Ну давай, презентуй –
          Кем ты был, чем себя прославил?
          Только сразу – базар фильтруй!
          Я не лох, я Апостол Павел.
          
          Я сказал: - Я был рекламист.
          Мой ответ его позабавил.
          - То есть, типа, авантюрист. -
          Усмехнулся Апостол Павел.
          
          Я сказал: - Нет, я двигал товар!
          И я действовал в рамках правил!
          Я ж работу людям давал,
          Я большое агентство возглавил!
          
          И вообще, реклама нужна:
          Раз клиент мне задачи ставил,
          Значит роль моя тоже важна! 
          Понимаешь, Апостол Павел?
          
          Я ж концепции создавал,
          И серьезные бренды пиарил!…
          Я Апостолу Павлу не врал.
          Как обычно – не врал, но лукавил.
          
          И апостол сказал: - Незачет.
          Все понты твои здесь не катят.
          Ты всю жизнь лишь дурил народ.
          Ты – никто. И на этом хватит.
          
          Этот тендер я проиграл.
          На душе стало пусто и просто.
          И тогда я ему сказал:
          Дай мне тридцать секунд, Апостол!	
          
          Я сейчас говорил фигню,
          Ну, привычка – втирать клиенту.
          Но я сам это все не ценю,
          И рекламу люблю не за это.
          
          Просто жизнь – суета сует.
          Страсти мелкие. Лица плоские.
          Мы одеты в неброский цвет,
          Да и мысли наши не броские,
          
          А реклама должна зажигать!
          Да, не честная. Не полезная.
          Но задача рекламы – играть,
          Делать жизнь людей интересной.
          
          И я верю, что нужен был.
          И профессией этой гордился!...
          Вот и все, я тему закрыл.
          Вот и все… Ну как, уложился?
          
          И апостол сказал: - Заценил.
          Ты ж коллега мне! Понимаешь?
          Тыщу лет и ночи и дни
          Тем же самым я занимаюсь,
          
          Как и вся контора моя!
          Правда, средства у нас другие,
          Но и шеф наш хочет и я, 
          Чтобы людям прикольно было.
          
          В общем, что стоишь-то? Войди!
          Отоспись, повидай папу с мамой,
          А с утра, уж прости, выходи
          На работу. Займешься рекламой.
          
          У нас тоже в рекламе бардак! –
          По секрету он тихо добавил: 
          - Но не парься, разрулим, чувак.
          Я ж не лох. Я Апостол Павел.</p>
