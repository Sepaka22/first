          <h2>Баллада о Бабах</h2>
          <p>Если был бы я, скажем, Бен Ладен
          Или был бы, к примеру, Гитлер 
          Или Сталин, будь он неладен –
          Я бы бабам по полной выдал!!!
          
          Я бы баб стрелял пулеметом,
          Чтобы бабы все трепетали!
          Я бы баб травил бы бойкотом –
          Потому что бабы достали!
          
          Я бы баб бы загнал бы в рабство,
          Объявил бы им всем диктатуру!
          Я бы баб штрафовал за бабство,
          Потому что все бабы – дуры!
          
          Я бы им запретил понтоваться,
          Через Думу провел бы указом,
          Чтоб не смели они пререкаться,
          Чтоб давали всегда и сразу!
          
          Я бы бабам сказал бы: «Бабы!
          Я ценю в вас вашу рельефность,
          Но зачем вам все эти траблы: 
          ПМС, феминизм и ревность?» 
          
          Я сказал бы: «Бабы, короче!
          Будем щас заниматься творчеством –
          Я вас буду любить, даже очень!
          Буду делать ячейки общества!»
          
          Я сказал бы: «А ну-ка постройтесь,
          Рассчитайтесь и лягте в койки!
          На порок и разврат настройтесь!
          Кто схалтурит – поставлю двойки!»
          
          Я бы баб поразил масштабом,
          Я бы баб вознес на вершины!....
          Только ходят одетыми бабы
          И, как правило, ходят мимо.
          
          И пустуют мои гаремы –
          Секса нету, будь он неладен!
          В общем, с бабами есть проблемы…
          В общем, жаль, что я не Бен Ладен!</p>
